-- Tabla de productos de papelería
CREATE TABLE PRODUCTOS_PAPELERIA (
    ID          NUMBER PRIMARY KEY,
    NOMBRE      VARCHAR2(100),
    PRECIO      NUMBER(10,2),
    STOCK       NUMBER
);

