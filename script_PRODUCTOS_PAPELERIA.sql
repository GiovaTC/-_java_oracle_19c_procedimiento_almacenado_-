--? 2. Procedimiento Almacenado (UPDATE)
CREATE OR REPLACE PROCEDURE ACTUALIZAR_PRODUCTO (
    P_ID        IN NUMBER,
    P_NOMBRE    IN VARCHAR2,
    P_PRECIO    IN NUMBER,
    P_STOCK     IN NUMBER
)
IS
BEGIN
    UPDATE PRODUCTOS_PAPELERIA
    SET 
        NOMBRE = P_NOMBRE,
        PRECIO = P_PRECIO,
        STOCK = P_STOCK
    WHERE ID = P_ID;

    COMMIT;
END;
